void printVector(const std::vector<uint8_t> &vec)
{
    std::stringstream sstr;
    std::string str = "";
    for (auto &v : vec)
    {
        sstr << std::setw(2) << std::setfill ('0') << std::hex << (int)v;
    }
    str = sstr.str();
    std::cout << str << "\n";
}

